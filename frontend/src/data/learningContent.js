/**
 * Returns the learning content for a given campaign level.
 * Levels 1-12: returns a fact object
 * Levels 13+: returns a question of appropriate difficulty
 */
export function getLevelContent(levelNumber) {
  if (LEVEL_FACTS[levelNumber]) {
    return { type: 'fact', ...LEVEL_FACTS[levelNumber] }
  }
  if (levelNumber >= 13) {
    const difficulty = levelNumber <= 30 ? 'easy' : levelNumber <= 60 ? 'normal' : 'hard'
    const pool = LEVEL_QUESTIONS.filter(q => q.difficulty === difficulty)
    if (!pool.length) return null
    // Rotate through pool based on level so each level gets a different question
    const idx = (levelNumber - 13) % pool.length
    return { type: 'quiz', ...pool[idx] }
  }
  return null
}

/**
 * Picks a random question of the given difficulty from the shared campaign pool.
 */
export function getEndlessQuestion(difficulty) {
  const pool = LEVEL_QUESTIONS.filter(q => q.difficulty === (difficulty ?? 'normal'))
  return pool[Math.floor(Math.random() * pool.length)]
}

