// ── Design builder ─────────────────────────────────────────────────────────────
function mk(id, name, series, blockType, desc, unlockSource, patternKey, colors, shopCost = 0) {
  const [c1 = null, c2 = null, c3 = null] = colors
  const rows = P[patternKey]
  if (!rows) {
    console.error(`Missing pattern: ${patternKey}`)
    return null
  }
  const pixelLayout = rows.map(row =>
    [...row].map(ch =>
      ch === '1' ? C[c1] ?? null :
      ch === '2' ? C[c2] ?? null :
      ch === '3' ? C[c3] ?? null : null
    )
  )
  const flat = pixelLayout.flat()
  const pixelCount = flat.filter(Boolean).length
  const counts = {}
  flat.forEach(c => { if (c) counts[c] = (counts[c] ?? 0) + 1 })
  const dominantColor = Object.entries(counts).sort((a, b) => b[1] - a[1])[0]?.[0] ?? null
  return { id, name, series, blockType, desc, unlockSource, pixelLayout, pixelCount, dominantColor, shopCost }
}

