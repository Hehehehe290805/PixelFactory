// The two designs offered at each 5-level milestone.
// Returns pairs: [[designA, designB], [designC, designD], ...]
export function getCampaignChoicePairs() {
  const pool = getCampaignChoiceDesigns()
  const pairs = []
  for (let i = 0; i < pool.length - 1; i += 2) {
    pairs.push([pool[i], pool[i + 1]])
  }
  return pairs
}

// In-level pixel cost for a design (uses BLOCK_TYPES levelCost as fallback)
const LEVEL_COSTS = {
  base: 13, doubler: 39, cross_amp: 32, color_checker: 26, greedy: 52,
  amplifier: 45, resonator: 55, reactor: 91, echo: 45, prism: 58,
  conductor: 72, splitter: 65, focus: 42, cluster: 55, forge: 78,
  overflow: 65, mirror: 58, catalyst: 78, void: 45,
}

const SERIES_COST_MULT = {
  space: 1.5, celestial: 1.5,
  symbols: 1.2, flowers: 1.1, animals: 1.1, buildings: 1.1,
  shapes: 1.0, weather: 1.0,
  trees: 0.75, food: 0.75, landscapes: 0.75, abstract: 0.75,
}

export function getDesignLevelCost(design, bargain = false) {
  const base = design.shopCost > 0 ? Math.ceil(design.shopCost * 0.15) : (LEVEL_COSTS[design.blockType] ?? 40)
  const mult = SERIES_COST_MULT[design?.series] ?? 1.0
  const cost = Math.max(5, Math.round(base * mult))
  return bargain ? Math.floor(cost * 0.8) : cost
}

